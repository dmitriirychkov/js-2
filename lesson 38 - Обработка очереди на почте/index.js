const peopleWaiting = ['Кристина', 'Олег', 'Кирилл', 'Мария', 'Светлана', 'Артем', 'Глеб'];

// Функция для выдачи посылки (удаляет первого в очереди)
function giveParcel() {
  const name = peopleWaiting.shift(); // удаляем первого и получаем его имя
  alert(`${name} получил(а) посылку. В очереди осталось ${peopleWaiting.length} человек.`);
}

// Функция для удаления из очереди без получения посылки (удаляет последнего)
function leaveQueueWithoutParcel() {
  const name = peopleWaiting.pop(); // удаляем последнего и получаем его имя
  alert(`${name} не получил(а) посылку и ушел(ла) из очереди`);
}

// 1. Кристина и Олег получили посылки
giveParcel(); // Кристина
